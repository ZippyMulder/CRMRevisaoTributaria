package crmrevisortributario.usuario;

import java.io.*;
import java.util.*;

import javax.persistence.*;

@Entity
@Table(name = "t_crt_usuario")
public class Usuario implements Serializable {

	/**
	 * 
	 */
	private static final long serialVersionUID = 7605529719014286005L;

	@Id
	@GeneratedValue
	@Column(name = "cd_usuario")
	private Integer	           codigo;
	
	@Column(name = "nm_usuario", length = 250, nullable = false)
	private String	           nome;
	
	@Column(name = "ds_login", length = 50, nullable = false)
	private String	           login;
	
	@Column(name = "ds_senha", length = 15, nullable = false)
	private String	           senha;
	
	@Column(name = "ds_email", length = 100, nullable = false)
	private String	           email;

	
	@ElementCollection(targetClass = String.class)
	@JoinTable(
	           name = "t_crt_permissao", 
			     uniqueConstraints = {@UniqueConstraint(columnNames = {"ds_permissao", "T_CRT_USUARIO_cd_usuario"})}, 
			     joinColumns = @JoinColumn(name = "T_CRT_USUARIO_cd_usuario"))
	@Column(name = "ds_permissao", length = 50)
	private Set<String>	     permissao	        = new HashSet<String>();


	
	public Integer getCodigo() {
		return codigo;
	}

	public void setCodigo(Integer codigo) {
		this.codigo = codigo;
	}

	public String getNome() {
		return nome;
	}

	public void setNome(String nome) {
		this.nome = nome;
	}

	public String getLogin() {
		return login;
	}

	public void setLogin(String login) {
		this.login = login;
	}

	public String getSenha() {
		return senha;
	}

	public void setSenha(String senha) {
		this.senha = senha;
	}

	public String getEmail() {
		return email;
	}

	public void setEmail(String email) {
		this.email = email;
	}

	public Set<String> getPermissao() {
		return permissao;
	}

	public void setPermissao(Set<String> permissao) {
		this.permissao = permissao;
	}

	@Override
	public int hashCode() {
		final int prime = 31;
		int result = 1;
		result = prime * result + ((codigo == null) ? 0 : codigo.hashCode());
		result = prime * result + ((email == null) ? 0 : email.hashCode());
		result = prime * result + ((login == null) ? 0 : login.hashCode());
		result = prime * result + ((nome == null) ? 0 : nome.hashCode());
		result = prime * result
				+ ((permissao == null) ? 0 : permissao.hashCode());
		result = prime * result + ((senha == null) ? 0 : senha.hashCode());
		return result;
	}

	@Override
	public boolean equals(Object obj) {
		if (this == obj)
			return true;
		if (obj == null)
			return false;
		if (getClass() != obj.getClass())
			return false;
		Usuario other = (Usuario) obj;
		if (codigo == null) {
			if (other.codigo != null)
				return false;
		} else if (!codigo.equals(other.codigo))
			return false;
		if (email == null) {
			if (other.email != null)
				return false;
		} else if (!email.equals(other.email))
			return false;
		if (login == null) {
			if (other.login != null)
				return false;
		} else if (!login.equals(other.login))
			return false;
		if (nome == null) {
			if (other.nome != null)
				return false;
		} else if (!nome.equals(other.nome))
			return false;
		if (permissao == null) {
			if (other.permissao != null)
				return false;
		} else if (!permissao.equals(other.permissao))
			return false;
		if (senha == null) {
			if (other.senha != null)
				return false;
		} else if (!senha.equals(other.senha))
			return false;
		return true;
	}
	
}
